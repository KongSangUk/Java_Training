package part2.ArithmeticOper;

public class AssignOperTest {
    public static void main(String[] args) {

        int a = 1;
        int b = 7;

        a++;
        ++a;
        a=a+1;
        a+=1;
        System.out.println("a = " + a);

        b--;
        --b;
        b=b-1;
        b-=1;
        System.out.println("b = " + b);

    }
}
